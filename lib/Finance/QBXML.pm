sub time2iso
{
  my ($self, $time) = @_;

  $time = time unless defined $time;

  my @date = gmtime($time);

  sprintf('%d-%02d-%02dT%02d:%02d:%02d',
          $date[5]+1900, $date[4]+1, @date[3,2,1,0]);
} # end time2iso

