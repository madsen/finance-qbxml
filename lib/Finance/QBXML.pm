sub format_XML
{
  my ($self, $node, $out) = @_;

  my $buffer;
  unless (defined $out) {
    $buffer = '';               # Indicate that we're opening $out
    open($out, '>', \$buffer);
  }

  require XML::Writer;

  my $w = XML::Writer->new(OUTPUT => $out, DATA_MODE => 1,
                           DATA_INDENT => $self->indent,
                           ENCODING => 'utf-8');

  $w->xmlDecl;
  $w->pi(qbxml => sprintf 'version="%s"', $self->version);
  $self->formatNode($w, QBXML => $node);
  $w->end;
  close $out if defined $buffer; # Don't close it if we didn't open it

  $buffer;
} # end format_XML

#---------------------------------------------------------------------
sub formatNode
{
  my ($self, $w, $tag, $node) = @_;

  my $reftype = (reftype($node) || '');

  if ($multipleChildren{$tag}) {
    # One element with multiple children in specified order (using _tag):
    my @attrs;

    if ($reftype eq 'HASH') {
      @attrs = map { $_ => $node->{$_} } grep { /^[[:lower:]]/ } keys %$node;
      # The arrayref of child nodes is stored in the '_' key:
      $node = $node->{_} || [];
    } # end if HASH

    $w->startTag($tag, @attrs);

    foreach my $n (@$node) {
      my $childTag = $n->{_tag} or croak "No _tag in child of $tag";
      $self->formatNode($w, $childTag => $n);
    }
    $w->endTag($tag);
  } # end if $multipleChildren{$tag}
  elsif ($reftype eq 'ARRAY') {
    # Multiple occurences of this tag:
    foreach my $n (@$node) {
      $self->formatNode($w, $tag => $n);
    }
  } # end elsif $node is ARRAY
  elsif ($reftype) {
    # An ordinary node formed from a hash reference:
    my (@attrs, $children);

    while (my ($k, $v) = each %$node) {
      if ($k =~ /^[[:lower:]]/) {
        push @attrs, $k, $v;
      } elsif (not $k =~ /^_/) {
        $children = 1;          # This is a node, not an attribute
        # FIXME check if valid element name?
      }
    } # end while my ($k, $v)

    if (not $children) {
      $w->emptyTag($tag, @attrs);
    } else {
      $w->startTag($tag, @attrs);

      # qbXML requires elements to appear in the correct order:
      foreach my $childTag (@{ $childElements{$tag} }) {
        $self->formatNode($w, $childTag => $node->{$childTag})
            if exists $node->{$childTag};
      } # end foreach $childTag

      $w->endTag($tag);
    } # end else node is not empty
  } # end elsif $node is a (hash) reference
  else {
    $w->dataElement($tag, $node);
  }
} # end formatNode

#---------------------------------------------------------------------
sub get_parser
{
  my ($self) = @_;

  require Finance::QBXML::Handler;
  require XML::SAX::ParserFactory;

  my $parser = XML::SAX::ParserFactory->parser(
    Handler => Finance::QBXML::Handler->new
  ) or croak "Unable to create parser";

  return $parser;
} # end get_parser

